#!/usr/bin/env node
/**
 * Walks the `ct` CLI yargs tree by recursively invoking the compiled binary
 * with `--help` and emits a Markdown reference between sentinel comments.
 *
 * Usage:
 *   node scripts/generate-cli-docs.js           # write docs in-place
 *   node scripts/generate-cli-docs.js --check   # exit 1 if docs are stale
 *
 * The CLI binary must be built first (`npm run build:cli`). --help does not
 * contact the HTTP server, so the Electron app need not be running.
 */
const { execFileSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const CLI_BIN = path.join(REPO_ROOT, 'dist/cli/cli/main.js');
const TARGETS = [
  { file: path.join(REPO_ROOT, 'docs/cli.md'), headerLevel: 2 },
  { file: path.join(REPO_ROOT, '.claude/cli-reference.md'), headerLevel: 3 },
];
const START_MARKER = '<!-- CT_CLI_DOCS:START -->';
const END_MARKER = '<!-- CT_CLI_DOCS:END -->';

function runHelp(argsPath) {
  return execFileSync('node', [CLI_BIN, ...argsPath, '--help'], {
    encoding: 'utf-8',
    stdio: ['ignore', 'pipe', 'ignore'],
  });
}

/**
 * Parse a `Commands:` block out of a yargs help string. Returns the list of
 * sub-command tokens (just the name after `ct x y`, no positionals).
 */
function parseSubcommands(helpText, parentPath) {
  const lines = helpText.split('\n');
  const startIdx = lines.findIndex((l) => l.trim() === 'Commands:');
  if (startIdx === -1) return [];

  const prefix = ['ct', ...parentPath].join(' ');
  const results = [];
  for (let i = startIdx + 1; i < lines.length; i++) {
    const line = lines[i];
    if (line.trim() === '') break;
    // Command lines are indented; continuation lines are indented further.
    if (!line.startsWith('  ct ')) continue;
    // Strip leading "  ct <parent path> " — what remains starts with the subcommand name.
    const afterPrefix = line.replace(new RegExp(`^  ${prefix} `), '');
    // Token = first whitespace-delimited word (drops positionals/description).
    const token = afterPrefix.split(/\s+/)[0];
    if (token && !results.includes(token)) results.push(token);
  }
  return results;
}

function renderSection(parentPath, helpText, headerLevel) {
  const name = parentPath.length === 0 ? 'ct' : `ct ${parentPath.join(' ')}`;
  const header = '#'.repeat(headerLevel) + ' `' + name + '`';
  // Keep help text verbatim in a fenced code block.
  return `${header}\n\n\`\`\`\n${helpText.trimEnd()}\n\`\`\`\n`;
}

function walk(parentPath, headerLevel, acc) {
  const helpText = runHelp(parentPath);
  acc.push(renderSection(parentPath, helpText, headerLevel));
  const subs = parseSubcommands(helpText, parentPath);
  for (const sub of subs) {
    walk([...parentPath, sub], headerLevel + 1, acc);
  }
}

function generate(baseHeaderLevel) {
  if (!fs.existsSync(CLI_BIN)) {
    throw new Error(`CLI binary not found at ${CLI_BIN}. Run \`npm run build:cli\` first.`);
  }
  const sections = [];
  walk([], baseHeaderLevel, sections);
  return [
    '<!-- Auto-generated by scripts/generate-cli-docs.js. Do not edit by hand; run `npm run docs`. -->',
    '',
    ...sections,
  ].join('\n').trimEnd() + '\n';
}

function spliceMarkers(source, generated) {
  const startIdx = source.indexOf(START_MARKER);
  const endIdx = source.indexOf(END_MARKER);
  if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
    throw new Error(`Missing ${START_MARKER} / ${END_MARKER} sentinels`);
  }
  const before = source.slice(0, startIdx + START_MARKER.length);
  const after = source.slice(endIdx);
  return `${before}\n${generated}\n${after}`;
}

function main() {
  const check = process.argv.includes('--check');
  let drift = false;

  for (const target of TARGETS) {
    const generated = generate(target.headerLevel);
    const source = fs.readFileSync(target.file, 'utf-8');
    const updated = spliceMarkers(source, generated);

    if (check) {
      if (source !== updated) {
        process.stderr.write(`[docs] ${path.relative(REPO_ROOT, target.file)} is out of date. Run \`npm run docs\`.\n`);
        drift = true;
      }
    } else if (source !== updated) {
      fs.writeFileSync(target.file, updated, 'utf-8');
      process.stdout.write(`[docs] wrote ${path.relative(REPO_ROOT, target.file)}\n`);
    }
  }

  if (check && drift) process.exit(1);
}

main();
